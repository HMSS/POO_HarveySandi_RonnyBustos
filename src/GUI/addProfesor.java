    private void bagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bagregarActionPerformed
        String id = tfidentificacion.getText();
        String nombre = tfnombre.getText();
        String contrasena = new String(tfcontrasena.getPassword());
        String departamento = comboboxdepartamento.getSelectedItem().toString();

        if(id.equals("") || nombre.equals("") || contrasena.equals("") || departamento.equals("")){
            String st="Todos los datos son necesarios";
            JOptionPane.showMessageDialog(null,st);
        }
        else{        	
        	brain.addProfessor(contrasena, id, nombre, departamento);
        	String st="Agregado con 峰ito";
            JOptionPane.showMessageDialog(null,st);
            tfidentificacion.setText("");
            tfnombre.setText("");
            tfcontrasena.setText("");
        }

    }//GEN-LAST:event_bagregarActionPerformed

