    public void actualizarDepartamentos(){	
    	comboboxdepartamento.removeAllItems();
    	ArrayList<Department>  temp =  brain.getDepartments();
		for (int i = 0; i < temp.size(); i++) {
			comboboxdepartamento.addItem(temp.get(i).getName());
		}
    }
    
    
    
